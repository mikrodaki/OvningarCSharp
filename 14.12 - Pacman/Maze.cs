		public bool IsPellet(int x, int y)
		{
			if (maze[y, x] == 2)
			{
				return true;
			}
			return false;
		}

		public bool DeletePellet(int x, int y)
		{
			if (maze[y, x] == 2)
			{
				maze[y, x] = 0;
				return true;
			}
			return false;
		}



		/* 
         * 
         * Draw
