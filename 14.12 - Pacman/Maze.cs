        public bool IsPellet(int x, int y)
        {
            if (maze[y, x] == 2)
            {
                return true;
            }
            return false;
        }

        public void DeletePellet(int x, int y)
        {
            if (maze[y, x] == 2)
                maze[y, x] = 0;
        }



        /* 
         * 
         * Draw
         * 
         * Draws the Pacman maze.
         * 
         */
        public void Draw()
        {
            for (int row = 0; row < consoleMaze.Length; row++)
                for (int col = 0; col < consoleMaze[row].Length; col++)
                {
                    Console.SetCursorPosition(col + Constants.X_SCREEN_POS, row + Constants.Y_SCREEN_POS);

                    if (consoleMaze[row][col] == '.')
                    {
                        Console.ForegroundColor = ConsoleColor.White;
                        Console.Write(".");
                        maxPoints++;
                    }
                    else
                    {
                        Console.ForegroundColor = ConsoleColor.Blue;
                        Console.Write(consoleMaze[row][col]);
                    }
                }
        }
